    def load_screen
        i = 0
        while i < 23
            system "clear"
            puts @loading[i]
            sleep(0.1)
            i += 1
        end
        system "clear"
    end

    def welcome_screen
        @intro.each do |i|
            system "clear"
            puts i
            sleep(0.5)
        end
    end

    def title_screen
      puts @intro.last
    end

    def exit_screen
      puts @exit_screen.sample
    end
